include_once("resource/database.php");

$gameno = $_POST['gameno'];
$above = explode(',', $_POST['above']);
$below = explode(',', $_POST['below']);
for ($i = 1; $i < count($above); $i++) {
	$temp_above = $above[$i];
	$temp_below = $below[$i];
	if ($temp_above > $temp_below) {
		$play = get($gameno, $i);
		mysql_query("UPDATE GAMESTATE SET ABOVESCORE='$temp_above', BELOWSCORE='$temp_below' WHERE GAMENO='$gameno' AND PLAYNO='$i'");
		updateGameChart($gameno, $play['SYSTEMPLAYNO'], $i, $temp_above, $temp_below, 'above');
	}
	elseif ($temp_above < $temp_below) {
		$play = get($gameno, $i);
		mysql_query("UPDATE GAMESTATE SET ABOVESCORE='$temp_above', BELOWSCORE='$temp_below' WHERE GAMENO='$gameno' AND PLAYNO='$i'");
		updateGameChart($gameno, $play['SYSTEMPLAYNO'], $i, $temp_above, $temp_below, 'below');
	}
}
echo json_encode(array('message' => 'Success'));

function get($gameno, $playno) {
